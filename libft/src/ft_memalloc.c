#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void	*fresh;

	if (!(fresh = malloc(sizeof(*fresh) * (int)size)))
		return (NULL);
	ft_bzero(fresh, size);
	return (fresh);
}
